// Member additions synthesized across the full app set (see
// scripts/build-segment-insights — the new apps' titles assigned to category
// insights, produced by an Opus synthesis pass over segment-meta/mm-new-titles).
const ADDITIONS = "src/data/segment-defs-additions.json";
type Additions = { extend: Record<string, string[]>; new: CatItemDef[] };

function applyAdditions(defs: SegmentDef[]) {
  let raw: string;
  try { raw = readFileSync(ADDITIONS, "utf8"); } catch { return; }
  const add = JSON.parse(raw) as Additions;
  const mm = defs.find((d) => d.slug === "meditation-mindfulness");
  if (!mm) return;
  for (const it of mm.items) {
    const extra = add.extend[it.id];
    if (extra) it.members.push(...extra);
  }
  for (const ni of add.new) {
    if (!mm.items.some((i) => i.id === ni.id)) mm.items.push(ni);
  }
}

// Additional segment syntheses authored after the original two, kept in an
// external JSON file (one entry per category slug) so the giant authored prose
// stays out of this source. Same shape as the hardcoded DEFS/SECTIONS above.
const EXTRA = "src/data/segment-defs-extra.json";
type ExtraEntry = { def: SegmentDef; sections: SectionDef[] };
function applyExtra(defs: SegmentDef[]) {
  let raw: string;
  try { raw = readFileSync(EXTRA, "utf8"); } catch { return; }
  const extra = JSON.parse(raw) as Record<string, ExtraEntry>;
  for (const [slug, entry] of Object.entries(extra)) {
    // The external entry is authoritative: replace any hardcoded DEF of the same
    // slug (the hardcoded one may have stale member titles), else append.
    const idx = defs.findIndex((d) => d.slug === slug);
    if (idx >= 0) defs[idx] = entry.def;
    else defs.push(entry.def);
    SECTIONS[slug] = entry.sections;
  }
}

function main() {
  applyAdditions(DEFS);
  applyExtra(DEFS);
  const products = JSON.parse(readFileSync(INSIGHTS, "utf8")) as ProductInsights[];
  const domains = JSON.parse(readFileSync(CATS, "utf8")) as { categories: { slug: string; apps: string[] }[] }[];
  const meta = JSON.parse(readFileSync(META, "utf8")) as Meta;

  const out: Record<string, unknown> = {};

  for (const def of DEFS) {
    // productIds in scope for this segment + pid → app name
    const cat = domains.flatMap((d) => d.categories).find((c) => c.slug === def.slug);
    if (!cat) { console.log(`!! no category ${def.slug}`); continue; }
    const pidName = new Map<string, string>();
    for (const app of cat.apps) {
      const m = meta[`${def.slug}:${app}`];
      if (m?.productId) pidName.set(m.productId, m.name);
    }
    const inScope = products.filter((p) => pidName.has(p.productId));

    // Publication gate: a category synthesis ships ONLY when every one of the
    // 10 catalog apps has a processed разбор fed by the full review sample
    // (500 per app, the pipeline cap). Partial data must never be synthesized.
    const MIN_APPS = 10;
    const MIN_REVIEWS_PER_APP = 500;
    const fullApps = inScope.filter(
      (p) =>
        (p as { balanced?: boolean }).balanced === true &&
        Array.isArray(p.insights) &&
        p.insights.length > 0 &&
        (p.reviewsScanned ?? 0) >= MIN_REVIEWS_PER_APP,
    );
    if (fullApps.length < MIN_APPS) {
      const short = inScope
        .filter((p) => (p.reviewsScanned ?? 0) < MIN_REVIEWS_PER_APP)
        .map((p) => `${pidName.get(p.productId)}(${p.reviewsScanned ?? 0})`);
      console.log(
        `[${def.slug}] GATED: ${fullApps.length}/${MIN_APPS} apps with ${MIN_REVIEWS_PER_APP}+ reviews — synthesis withheld` +
          (short.length ? ` (short: ${short.join(", ")})` : ""),
      );
      continue;
    }

    // title → {obs, appName, evidence}
    const byTitle = new Map<string, { obs: number; app: string; evidence: Evidence[] }>();
    let reviewsScanned = 0;
    for (const p of inScope) {
      reviewsScanned += p.reviewsScanned ?? 0;
      for (const i of p.insights) {
        byTitle.set(i.title, {
          obs: i.observationCount ?? i.evidence.length,
          app: pidName.get(p.productId)!,
          evidence: i.evidence ?? [],
        });
      }
    }

    const items = def.items.map((it) => {
      const apps = new Set<string>();
      let observationCount = 0;
      const evidence: (Evidence & { app: string })[] = [];
      const missing: string[] = [];
      for (const title of it.members) {
        const hit = byTitle.get(title);
        if (!hit) { missing.push(title); continue; }
        apps.add(hit.app);
        observationCount += hit.obs;
        const top = [...hit.evidence].sort((a, b) => b.rating - a.rating)[0] ?? hit.evidence[0];
        if (top) evidence.push({ ...top, app: hit.app });
      }
      if (missing.length) console.log(`  [${def.slug}/${it.id}] UNMATCHED members:\n    - ${missing.join("\n    - ")}`);
      evidence.sort((a, b) => b.rating - a.rating);
      return {
        id: it.id,
        theme: it.theme,
        title: it.title,
        body: it.body,
        apps: [...apps],
        observationCount,
        evidence: evidence.slice(0, 4),
      };
    });

    // Drop insights whose members matched no current review (no evidence) — a
    // stale member title must not surface as an empty "0 наблюдений" row.
    const dropped = items.filter((it) => it.observationCount === 0 || it.evidence.length === 0);
    if (dropped.length) console.log(`  [${def.slug}] dropped ${dropped.length} empty item(s): ${dropped.map((d) => d.id).join(", ")}`);
    const liveItems = items.filter((it) => it.observationCount > 0 && it.evidence.length > 0);

    // If nothing survived (all member titles stale vs current data), withhold
    // the synthesis entirely rather than ship an empty summary.
    if (!liveItems.length) {
      console.log(`[${def.slug}] WITHHELD: 0 items with evidence (stale member titles)`);
      continue;
    }

    // Thread the resolved items into narrative editorial sections. Items are
    // sorted loudest-first within a section; any item not placed in a section
    // falls into a trailing catch-all so nothing is silently dropped.
    const itemById = new Map(liveItems.map((it) => [it.id, it]));
    const placed = new Set<string>();
    const sectionDefs = SECTIONS[def.slug] ?? [];
    const sections = sectionDefs
      .map((s) => {
        const secItems = s.itemIds
          .map((id) => itemById.get(id))
          .filter((it): it is (typeof items)[number] => Boolean(it))
          .sort((a, b) => b.observationCount - a.observationCount);
        for (const id of s.itemIds) placed.add(id);
        return { id: s.id, heading: s.heading, dek: s.dek, items: secItems };
      })
      .filter((s) => s.items.length > 0);
    const orphans = liveItems.filter((it) => !placed.has(it.id));
    if (orphans.length) {
      console.log(`  [${def.slug}] ${orphans.length} item(s) not in any section: ${orphans.map((o) => o.id).join(", ")}`);
      sections.push({ id: "sec-other", heading: "Прочие наблюдения", dek: "", items: orphans.sort((a, b) => b.observationCount - a.observationCount) });
    }

    out[def.slug] = {
      slug: def.slug,
      lead: def.lead,
      asOf: new Date().toISOString().slice(0, 10),
      appsCount: inScope.length,
      reviewsScanned,
      items: liveItems,
      sections,
    };
    const totalObs = liveItems.reduce((s, i) => s + i.observationCount, 0);
    console.log(`[${def.slug}] ${liveItems.length} category insights · ${inScope.length} apps · ${reviewsScanned} reviews · ${totalObs} obs aggregated`);
  }

  writeFileSync(OUT, JSON.stringify(out, null, 2) + "\n");
  console.log(`wrote ${OUT}`);
}

main();
